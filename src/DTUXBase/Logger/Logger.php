namespace DTUXBase\Logger;
use DTUXBase\Document\Logger as Logs;

class Logger{

    private $service;
    /**
     * Detailed debug information
     */
    const DEBUG = 100;

    /**
     * Interesting events
     *
     * Examples: User logs in, SQL logs.
     */
    const INFO = 200;

    /**
     * Uncommon events
     */
    const NOTICE = 250;

    /**
     * Exceptional occurrences that are not errors
     *
     * Examples: Use of deprecated APIs, poor use of an API,
     * undesirable things that are not necessarily wrong.
     */
    const WARNING = 300;

    /**
     * Runtime errors
     */
    const ERROR = 400;

    /**
     * Critical conditions
     *
     * Example: Application component unavailable, unexpected exception.
     */
    const CRITICAL = 500;

    /**
     * Action must be taken immediately
     *
     * Example: Entire website down, database unavailable, etc.
     * This should trigger the SMS alerts and wake you up.
     */
    const ALERT = 550;

    /**
     * Urgent alert.
     */
    const EMERGENCY = 600;

    /**
     * Monolog API version
     *
     * This is only bumped when API breaks are done and should
     * follow the major version of the library
     *
     * @var int
     */
    const API = 1;

    /**
     * Logging levels from syslog protocol defined in RFC 5424
     *
     * @var array $levels Logging levels
     */
    protected static $levels = array(
        100 => 'DEBUG',
        200 => 'INFO',
        250 => 'NOTICE',
        300 => 'WARNING',
        400 => 'ERROR',
        500 => 'CRITICAL',
        550 => 'ALERT',
        600 => 'EMERGENCY',
    );

    /**
     * @var DateTimeZone
     */
    protected static $timezone;

    protected $name;


    /**
     * @param string $name       The logging channel
     * @param array  $handlers   Optional stack of handlers, the first one in the array is called first, etc.
     * @param array  $processors Optional array of processors
     */
    public function __construct($name,$service)
    {
        $this->name = $name;
        $this->service = $service;

    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }


    /**
     * Adds a log record.
     *
     * @param  integer $level   The logging level
     * @param  string  $message The log message
     * @param  array   $context The log context
     * @return Boolean Whether the record has been processed
     */
    public function addRecord($level, $message,$controller,$action)
    {

        $record = array(
            'message'           => (string) $message,
            'controller' 	    => $controller,
            'action'		    => $action,
            'module' 		    => __NAMESPACE__,
            'level'             => $level,
            'level_name'        => static::getLevelName($level),
            'channel'           => $this->name,
            'extra'             => array(),
            'ip'                => $_SERVER['REMOTE_ADDR']

        );

        $entity = new Logs();
        $entity->setMessage($record['message']);
        $entity->setController($record['controller']);
        $entity->setAction($record['action']);
        $entity->setModule($record['module']);
        $entity->setLevel($record['level']);
        $entity->setLevelName($record['level_name']);
        $entity->setChannel($record['channel']);
        $entity->setExtra($record['extra']);
        $entity->setIp($record['ip']);

        $this->service->save($entity);

        return true;
    }

    /**
     * Adds a log record at the INFO level.
     *
     * @param  string  $message The log message
     * @param  array   $context The log context
     * @return Boolean Whether the record has been processed
     */
    public function addInfo($message, array $context = array())
    {
        return $this->addRecord(static::INFO, $message, $context);
